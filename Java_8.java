public class Java_8 {
    public static void main(String[]args){
        int a = 50, b = 40;
        System.out.println("---Before Swap---");
        System.out.println("First Number :" + a);
        System.out.println("Second Number:" + b);
        int temp = a;
        a = b;
        b = temp;
        System.out.println("---After Swap---");
        System.out.println("First Number :" + a);
        System.out.println("Second Number:" + b);


    }
}
